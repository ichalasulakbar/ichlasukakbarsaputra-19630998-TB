private void tampil_data() {
        int row = tabelgudang.getRowCount();
        for(int a= 0; a<row;a++){
            model.removeRow(0);
        }
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/gudang","root","");;
            ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM gudang");
            while(rs.next()){
                String data []= {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5)};
                model.addRow(data);
            }
                } catch (SQLException ex) {
            Logger.getLogger(gudang.class.getName()).log(Level.SEVERE, null, ex);
        }
    }



    
    
    
    

    private void tabelgudangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelgudangMouseClicked
        // TODO add your handling code here:
        int i = tabelgudang.getSelectedRow();
        
        if(i>-1){
            No_Order.setText(model.getValueAt(i, 0).toString());
            Jumlah.setText(model.getValueAt(i, 1).toString());
            Tgl_Masuk.setText(model.getValueAt(i, 2).toString());
            Tgl_Keluar.setText(model.getValueAt(i, 3).toString());
            No_Barang.setText(model.getValueAt(i, 4).toString());
        }
    }//GEN-LAST:event_tabelgudangMouseClicked

